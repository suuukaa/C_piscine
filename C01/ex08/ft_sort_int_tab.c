void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	swap;

	j = 0;
	while (j < size)
	{
		i = 0;
		while (i < size)
		{
			if (tab[i] > tab[j])
			{
				swap = tab[i];
				tab[i] = tab[j];
				tab[j] = swap;
			}
			i++;
		}
		j++;
	}
}
